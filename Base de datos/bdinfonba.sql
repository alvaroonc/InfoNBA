create database if not exists bdinfonba;
use bdinfonba;

CREATE TABLE estadios (
  id integer PRIMARY KEY AUTO_INCREMENT,
  nombre varchar(255),
  capacidad integer,
  ciudad varchar(255),
  imagen varchar(255)
);

CREATE TABLE equipos (
  id integer PRIMARY KEY AUTO_INCREMENT,
  nombre varchar(255),
  campeonatos integer,
  entrenador varchar(255),
  fundacion varchar(255),
  ciudad varchar(255),
  logo varchar(255),
  color_principal varchar(255),
  conferencia varchar(255),
  victorias integer,
  derrotas integer,
  id_estadio integer,
  FOREIGN KEY (id_estadio) REFERENCES estadios(id) ON DELETE CASCADE
);

CREATE TABLE jugadores (
  id integer PRIMARY KEY AUTO_INCREMENT,
  id_equipo integer,
  nombre varchar(255),
  apellido varchar(255),
  posicion varchar(255),
  altura integer,
  peso decimal(10,2),
  dorsal integer,
  partidos_jugados integer,
  puntos integer,
  asistencias integer,
  rebotes integer,
  FOREIGN KEY (id_equipo) REFERENCES equipos(id)
);

CREATE TABLE partidos (
  id integer PRIMARY KEY AUTO_INCREMENT,
  id_equipo_local integer,
  id_equipo_visitante integer,
  id_estadio integer,
  puntos_local integer,
  puntos_visitante integer,
  FOREIGN KEY (id_equipo_local) REFERENCES equipos(id) ON DELETE CASCADE,
  FOREIGN KEY (id_equipo_visitante) REFERENCES equipos(id) ON DELETE CASCADE,
  FOREIGN KEY (id_estadio) REFERENCES estadios(id) ON DELETE CASCADE
);

